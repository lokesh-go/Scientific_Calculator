    private void button_equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_equalActionPerformed
        // TODO add your handling code here:
        if(p==1)
        {
            String text = result_view.getText();
            double val2 = Double.parseDouble(text);
            result_view.setText(Double.toString(valp+val2));
            p=0;
        }
        else if(m==1)
        {
            String text = result_view.getText();
            double val2 = Double.parseDouble(text);
            result_view.setText(Double.toString(valp-val2));
            m=0;
        }
        else if(mul==1)
        {
            String text = result_view.getText();
            double val2 = Double.parseDouble(text);
            result_view.setText(Double.toString(valp*val2));
            mul=0;
        }
        else if(div==1)
        {
                String text = result_view.getText();
            double val2 = Double.parseDouble(text);
            if(val2!=0)
            {
            result_view.setText(Double.toString(valp/val2));
            mul=0;
            
            }
            else
            {
                result_view.setText("0");
                JOptionPane.showMessageDialog(null, "Diveded by zero");
                valp=0;
            }
            
        }
        else if(pw==1)
        {
             String text = result_view.getText();
            double val2 = Double.parseDouble(text);
            result_view.setText(Double.toString(Math.pow(valp, val2)));
            pw=0;
        }
        else if(per==1)
        {
             String text = result_view.getText();
            double val2 = Double.parseDouble(text);
            result_view.setText(Double.toString((valp*val2)/100.00));
            per=0;
        }
    }//GEN-LAST:event_button_equalActionPerformed

