    private void primeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_primeActionPerformed
        // TODO add your handling code here:
        String text = result_view.getText();
        boolean check = text.contains(".");
        if(check)
        {
            JOptionPane.showMessageDialog(null, "Not Prime");
            result_view.setText("0");
        }
        else
        {
            int c=0;
            int vall = Integer.parseInt(text);
            if(vall==2)
            {
                JOptionPane.showMessageDialog(null, "Number is Prime");
                 result_view.setText("0");
            }
            else if(vall%2==0)
            {
                 JOptionPane.showMessageDialog(null, "Number is not Prime");
                 result_view.setText("0");
            }
            else
            {
                for(int i=3;i*i<vall;i=i+2)
                {
                    if(vall%i==0)
                    {
                        c++;
                        break;
                    }
                }
                if(c==0)
                {
                      JOptionPane.showMessageDialog(null, "Number is Prime");
                 result_view.setText("0");
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Number is not Prime");
                 result_view.setText("0");
                }
               
                }
        }
    }//GEN-LAST:event_primeActionPerformed

